                            bars( they don't all need to be down bars.) Preferably the
                            pullback makes sure significant downward progress, but it must
                            not drop below the 8:30AM price where the initial move began. If
                            the initial move was down, we want to see the price rally off
                            the low of the Initial move and stay above low that for at least
                            5 bars. Preferably, the pullback makes significant upward
                            progress, but it most not rise above the 8:30AM price, when the
                            Initial down move began.
